
public class type_casting {
	 public static void main(String[] args) {
		double res = 0.14f + 4.6;
		System.out.println(res);
		
		double a = 128.5;
		int b = (int)a;
		System.out.println(b);
		
		double x = 125.3f;
		System.out.println(x);
		
		float y = 125.9f;
		System.out.println(y);
		
		int r = (int)125.7;
		System.out.println(r);
	}
}
